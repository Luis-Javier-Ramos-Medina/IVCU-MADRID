def normalizar_0_10(serie, invertir=False):
    """Normaliza una serie al rango 0–10. invertir=True para indicadores donde
    más valor significa menos vulnerabilidad (p.ej. renta, árboles)."""
    minimo, maximo = serie.min(), serie.max()
    if maximo == minimo:
        return pd.Series([5.0] * len(serie), index=serie.index)
    normalizado = (serie - minimo) / (maximo - minimo) * 10
    return (10 - normalizado) if invertir else normalizado


def pivotear_formato_ancho(df, magnitud_cod, nombre_col):
    """Convierte el formato ancho D01…D31/V01…V31 a formato largo y
    devuelve la media mensual por estación."""
    df = df[df['MAGNITUD'] == magnitud_cod].copy()
    if len(df) == 0:
        return pd.DataFrame(columns=['ESTACION', 'ANO', 'MES', nombre_col])
    registros = []
    for i in range(1, 32):
        dia, flag = f'D{i:02d}', f'V{i:02d}'
        if dia not in df.columns:
            continue
        tmp = df[['ESTACION', 'ANO', 'MES', dia, flag]].copy()
        tmp.columns = ['ESTACION', 'ANO', 'MES', 'valor', 'flag']
        registros.append(tmp)
    largo = pd.concat(registros, ignore_index=True)
    largo = largo[largo['flag'] == 'V']
    largo['valor'] = pd.to_numeric(largo['valor'], errors='coerce')
    largo = largo.dropna(subset=['valor'])
    return (
        largo.groupby(['ESTACION', 'ANO', 'MES'])['valor']
        .mean().reset_index().rename(columns={'valor': nombre_col})
    )


def idw_interpolar(estaciones_df, centros_barrios, col_valor, power=2):
    """
    Interpolación IDW (Inverse Distance Weighting) desde estaciones de medición
    hacia los centroides de los barrios.

    Parámetros
    ----------
    estaciones_df : DataFrame con columnas ['lon', 'lat', col_valor]
    centros_barrios : DataFrame con columnas ['lon', 'lat'] indexado igual que ivcu
    col_valor : nombre de la columna con el valor a interpolar
    power : exponente IDW (2 = cuadrático, estándar)

    Devuelve
    --------
    Series con el valor interpolado para cada fila de centros_barrios
    """
    est = estaciones_df.dropna(subset=['lon', 'lat', col_valor])
    if len(est) == 0:
        print(f'     ⚠ Sin estaciones con coordenadas para IDW — usando media global')
        return pd.Series([estaciones_df[col_valor].mean()] * len(centros_barrios),
                         index=centros_barrios.index)

    est_coords = est[['lon', 'lat']].values
    est_vals   = est[col_valor].values
    bar_coords = centros_barrios[['lon', 'lat']].values

    resultados = []
    for bc in bar_coords:
        distancias = np.sqrt(((est_coords - bc) ** 2).sum(axis=1))
        # Si un barrio coincide exactamente con una estación, devolver ese valor
        if distancias.min() < 1e-6:
            resultados.append(est_vals[distancias.argmin()])
            continue
        pesos = 1.0 / (distancias ** power)
        valor_idw = np.sum(pesos * est_vals) / np.sum(pesos)
        resultados.append(valor_idw)

    return pd.Series(resultados, index=centros_barrios.index)


