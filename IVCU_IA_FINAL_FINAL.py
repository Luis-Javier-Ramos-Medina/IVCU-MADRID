def procesar_barrios_shapefile():
    print('\n  2a. Shapefile de barrios...')
    shp_files = [f for f in os.listdir('data/spatial/') if f.endswith('.shp')]
    if not shp_files:
        raise FileNotFoundError('No se encontró .shp en data/spatial/')
    gdf = gpd.read_file(f'data/spatial/{shp_files[0]}')
    print(f'     CRS original: {gdf.crs} | Barrios: {len(gdf)}')
    gdf = limpiar_columnas(gdf)
    col_map = {}
    for col in gdf.columns:
        cu = col.upper()
        if 'CODDIS' in cu or cu == 'COD_DIS':
            col_map['cod_distrito'] = col
        elif 'NOMDIS' in cu or cu == 'NOMDIST':
            col_map['nombre_distrito'] = col
        elif 'COD_BAR' in cu or cu == 'CODBAR':
            col_map['cod_barrio'] = col
        elif cu in ('NOMBRE', 'NOMBAR', 'DESC_BARRIO'):
            col_map['nombre_barrio'] = col
        elif 'AREA' in cu or 'SHAPE_AREA' in cu:
            col_map['area_m2'] = col
    if 'cod_barrio' not in col_map and 'COD_DISBAR' in gdf.columns:
        col_map['cod_barrio'] = 'COD_DISBAR'
    if 'area_m2' not in col_map:
        gdf_utm = gdf.to_crs(epsg=25830) if gdf.crs and gdf.crs.to_epsg() != 25830 else gdf
        gdf['area_calc'] = gdf_utm.geometry.area
        col_map['area_m2'] = 'area_calc'
    gdf_base = gdf[[col_map[k] for k in col_map if k != 'geometry'] + ['geometry']].copy()
    gdf_base.rename(columns={v: k for k, v in col_map.items()}, inplace=True)
    for c in ['nombre_distrito', 'nombre_barrio']:
        if c in gdf_base.columns:
            gdf_base[c] = limpiar_texto(gdf_base[c].astype(str))
    gdf_base['cod_distrito'] = pd.to_numeric(gdf_base['cod_distrito'], errors='coerce').astype('Int64')
    gdf_base['area_m2']      = pd.to_numeric(gdf_base['area_m2'],      errors='coerce')
    gdf_utm = gdf_base.to_crs(epsg=25830) if gdf_base.crs and gdf_base.crs.to_epsg() != 25830 else gdf_base
    gdf_base['centroide_x'] = gdf_utm.geometry.centroid.x
    gdf_base['centroide_y'] = gdf_utm.geometry.centroid.y
    gdf_wgs = gdf_base.to_crs(epsg=4326)
    gdf_base['lon'] = gdf_wgs.geometry.centroid.x
    gdf_base['lat'] = gdf_wgs.geometry.centroid.y
    print(f'     ✓ {len(gdf_base)} barrios procesados')
    return gdf_base


def procesar_vulnerabilidad_referencia():
    """
    FIX v6: el campo BARRIO del dataset oficial tiene espacios iniciales
    (p.ej. "   022. Las Acacias") que impedían extraer el código numérico.
    Solución: strip() antes de extraer código y nombre.
    También se normaliza el nombre eliminando artículos iniciales (Las, Los,
    La, El, Del) para maximizar el match con los nombres del shapefile.
    """
    print('\n  2b. Vulnerabilidad oficial (referencia)...')
    df = pd.read_csv('data/raw/vulnerabilidad.csv', encoding='latin-1', sep=';')
    df = limpiar_columnas(df)
    df = df[df['Año'] == df['Año'].max()].copy()
    df['Vulnerabilidad'] = pd.to_numeric(
        df['Vulnerabilidad'].astype(str).str.replace(',', '.', regex=False), errors='coerce'
    )

    # FIX v6: strip() para eliminar espacios iniciales antes de extraer código
    df['BARRIO_strip'] = df['BARRIO'].astype(str).str.strip()

    # Extraer código numérico (p.ej. "022. Las Acacias" → 22)
    df['cod_barrio_num'] = pd.to_numeric(
        df['BARRIO_strip'].str.extract(r'^(\d+)\.').squeeze(), errors='coerce'
    ).astype('Int64')

    # Nombre normalizado: eliminar el código inicial y artículos (Las, Los, La, El, Del)
    def extraer_nombre(barrio_str):
        nombre = barrio_str.split('.', 1)[-1] if '.' in barrio_str else barrio_str
        nombre = normalizar_nombre(nombre)
        # Eliminar artículos iniciales para mejorar el match
        for art in ('las ', 'los ', 'la ', 'el ', 'del ', 'de '):
            if nombre.startswith(art):
                nombre = nombre[len(art):]
                break
        return nombre.strip()

    df['nombre_barrio_norm'] = df['BARRIO_strip'].apply(extraer_nombre)

    resultado = df[['cod_barrio_num', 'nombre_barrio_norm', 'Ranking', 'Vulnerabilidad']].copy()
    resultado.columns = ['cod_barrio_vuln', 'nombre_barrio_norm', 'ranking_ref', 'ivcu_ref']
    print(f'     ✓ {len(resultado)} barrios con vulnerabilidad de referencia')
    print(f'     Códigos ejemplo: {resultado["cod_barrio_vuln"].dropna().head(5).tolist()}')
    return resultado


def procesar_padron_barrio():
    """
    FIX v4: el padrón siempre se devuelve a nivel barrio (cod_dist_barrio de 4 dígitos).
    Si faltaran datos para algún barrio, se imputa la media del DISTRITO, no la global.
    """
    print('\n  2c. Padrón municipal (% mayores 65)...')
    df = pd.read_csv('data/raw/padron.csv', encoding='latin-1', sep=';', low_memory=False)
    df = limpiar_columnas(df)
    df.rename(columns={df.columns[0]: 'COD_DISTRITO'}, inplace=True)
    df['poblacion'] = (
        df['ESPANOLESHOMBRES'].fillna(0) + df['ESPANOLESMUJERES'].fillna(0) +
        df['EXTRANJEROSHOMBRES'].fillna(0) + df['EXTRANJEROSMUJERES'].fillna(0)
    )
    df['edad_num']       = pd.to_numeric(df['COD_EDAD_INT'].astype(str).str.strip(), errors='coerce')
    df['COD_DIST_BARRIO'] = pd.to_numeric(df['COD_DIST_BARRIO'], errors='coerce').astype('Int64')
    df['COD_DISTRITO_num'] = pd.to_numeric(df['COD_DISTRITO'],    errors='coerce').astype('Int64')

    pob_total   = df.groupby('COD_DIST_BARRIO')['poblacion'].sum().reset_index().rename(
        columns={'poblacion': 'pob_total'})
    pob_mayores = df[df['edad_num'] >= 65].groupby('COD_DIST_BARRIO')['poblacion'].sum().reset_index().rename(
        columns={'poblacion': 'pob_65mas'})
    dist_map    = df[['COD_DIST_BARRIO', 'COD_DISTRITO_num']].drop_duplicates()

    resultado = pob_total.merge(pob_mayores, on='COD_DIST_BARRIO', how='left')
    resultado = resultado.merge(dist_map, on='COD_DIST_BARRIO', how='left')
    resultado['pob_65mas']      = resultado['pob_65mas'].fillna(0)
    resultado['pct_mayores_65'] = (resultado['pob_65mas'] / resultado['pob_total'] * 100).round(2)
    resultado.rename(columns={'COD_DIST_BARRIO': 'cod_dist_barrio',
                               'COD_DISTRITO_num': 'cod_distrito_padron'}, inplace=True)
    print(f'     ✓ {len(resultado)} barrios | % mayores 65: '
          f'{resultado["pct_mayores_65"].min():.1f}%–{resultado["pct_mayores_65"].max():.1f}%')
    return resultado


def procesar_renta_barrio():
    print('\n  2d. Renta neta anual por barrio...')
    df = pd.read_csv('data/raw/socioeconomico.csv', encoding='latin-1', sep=';', low_memory=False)
    df = limpiar_columnas(df)
    df.rename(columns={df.columns[0]: 'orden'}, inplace=True)
    col_rename = {}
    for c in df.columns:
        lc = c.lower().replace('ã±', 'ñ').replace('ã­', 'í').replace('ã©', 'é')
        if 'indicador_nivel1' in lc:   col_rename[c] = 'indicador_nivel1'
        elif 'indicador_nivel2' in lc: col_rename[c] = 'indicador_nivel2'
        elif 'valor_indicador' in lc:  col_rename[c] = 'valor_indicador'
        elif lc.strip() in ('año', 'a±o'): col_rename[c] = 'año'
        elif 'cod_barrio' in lc:       col_rename[c] = 'cod_barrio'
        elif 'cod_distrito' in lc:     col_rename[c] = 'cod_distrito'
    df.rename(columns=col_rename, inplace=True)
    mask_renta = df['indicador_nivel1'].astype(str).str.contains('Renta|renta', na=False, case=False)
    mask_neta  = df['indicador_nivel2'].astype(str).str.contains('Neta|neta|Net', na=False, case=False)
    renta_df   = df[mask_renta & mask_neta].copy()
    if len(renta_df) == 0:
        renta_df = df[mask_renta].copy()
    renta_df['año'] = pd.to_numeric(renta_df.get('año', pd.Series()), errors='coerce')
    anyo_max = renta_df['año'].max()
    renta_df = renta_df[renta_df['año'] == anyo_max].copy()
    renta_df['valor_num'] = pd.to_numeric(
        renta_df['valor_indicador'].astype(str)
        .str.replace('.', '', regex=False).str.replace(',', '.', regex=False),
        errors='coerce'
    )
    if 'cod_barrio' in renta_df.columns:
        renta_bar = (
            renta_df.dropna(subset=['cod_barrio', 'valor_num'])
            .assign(cod_barrio=lambda x: pd.to_numeric(x['cod_barrio'], errors='coerce'))
            .dropna(subset=['cod_barrio'])
        )
    else:
        renta_bar = pd.DataFrame()

    if len(renta_bar) >= 20:
        resultado = (
            renta_bar.groupby('cod_barrio')['valor_num']
            .mean().reset_index()
            .rename(columns={'valor_num': 'renta_neta', 'cod_barrio': 'cod_barrio_ref'})
        )
        nivel = 'barrio'
    else:
        renta_dist = (
            renta_df.dropna(subset=['cod_distrito', 'valor_num'])
            .assign(cod_distrito=lambda x: pd.to_numeric(x['cod_distrito'], errors='coerce'))
            .dropna(subset=['cod_distrito'])
        )
        resultado = renta_dist.groupby('cod_distrito')['valor_num'].mean().reset_index().rename(
            columns={'valor_num': 'renta_neta'})
        nivel = 'distrito'
    print(f'     ✓ Año {int(anyo_max)} | nivel: {nivel} | '
          f'rango: {resultado["renta_neta"].min():.0f}€–{resultado["renta_neta"].max():.0f}€')
    return resultado, nivel


