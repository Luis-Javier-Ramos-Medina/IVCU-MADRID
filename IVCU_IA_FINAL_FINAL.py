def normalizar_nombre(s):
    """Normaliza un nombre para comparación fuzzy: minúsculas, sin tildes, sin espacios extra."""
    import unicodedata
    s = str(s).strip().lower()
    s = ''.join(c for c in unicodedata.normalize('NFD', s) if unicodedata.category(c) != 'Mn')
    s = s.replace('-', ' ').replace('_', ' ')
    s = ' '.join(s.split())
    return s


